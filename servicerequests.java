    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd-MMM-yyyy");  
        LocalDateTime now = LocalDateTime.now();  
        String today=dtf.format(now);
        int r_id=Integer.parseInt(tf_requestno.getText());
        try
        {
           con=new Connector().getCon();
           ps=con.prepareStatement("UPDATE `asdproject`.`patient_need_requests` SET `nr_status` = 'Fulfilled', `nr_fulfilled_date` =?, `nr_fulfilled_by_volunteer_id` =? WHERE (`nr_id` =?);");
           ps.setString(1, today);
           ps.setInt(2,v_id);
           ps.setInt(3,r_id);
           int rs=ps.executeUpdate();
           if(rs!=0)
           {
               JOptionPane.showMessageDialog(null,"Need Completed");
               tf_nameoftheperson.setText("");
               tf_requestno.setText("");
               ta_requestdetails1.setText("");
               model.removeRow(RowIndex);
           }
           else
             JOptionPane.showMessageDialog(null,"patient's request not Completed ");  
        }
        catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
           // e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        }
    }//GEN-LAST:event_jButton1MouseClicked

