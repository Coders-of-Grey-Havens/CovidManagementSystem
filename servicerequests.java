    public void fetch()
    {
        try
        {
           con=new Connector().getCon();
           ps=con.prepareStatement("SELECT c_local_body_name_id FROM asdproject.citizen_list WHERE c_id=?");
          ps.setString(1,Integer.toString(v_id));
          rs=ps.executeQuery();
          rs.next();
          v_dist_id=rs.getInt("c_local_body_name_id");
           ps=con.prepareStatement("Select n.nr_id AS Request_Number, c.c_fullname AS Persons_Name, n.nr_subject AS Request_Subject,n.nr_details AS Request_Details, n.nr_application_date AS RequestDate from asdproject.patient_need_requests n,asdproject.citizen_list c where n.c_id=c.c_id and c.c_local_body_name_id=? and nr_status='Request Placed'; ");
           ps.setString(1,Integer.toString(v_dist_id));
           rs=ps.executeQuery();
           t_pendingrequests.setModel(DbUtils.resultSetToTableModel(rs));
           if(!rs.first())
               JOptionPane.showMessageDialog(null,"No Pending Service Request at your location ");
        }
        catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        }
    
    }
