    private void getdata(int id)
    {
        try
        {
            con = new Connector().getCon();
            stmt= con.prepareStatement("SELECT * FROM `asdproject`.`citizen_list` WHERE c_id=?;");
            stmt.setInt(1,id);
            ResultSet rs = stmt.executeQuery();
            if(rs.next())
            {
            tf_fullname.setText(rs.getString(2));
            tf_email.setText(rs.getString(3));
//            tf_username.setText(rs.getString(4));
//            tf_password.setText(rs.getString(5));
            tf_phonenumber.setText(rs.getString(6));
            tf_gender.setText(rs.getString(7));
            tf_blood_group.setText(rs.getString(8));
            tf_dateofbirth.setText(rs.getString(9));
            int d_id=Integer.parseInt(rs.getString(10));
            int bt_id=Integer.parseInt(rs.getString(11));
            int b_id=Integer.parseInt(rs.getString(12));
            tf_wardnumber.setText(rs.getString(13));
            ta_address.setText(rs.getString(14));
            stmt= con.prepareStatement("SELECT dist_name FROM asdproject.districts where dist_id=?;");
            stmt.setInt(1,d_id);
            rs = stmt.executeQuery();
            if(rs.next())
                tf_district.setText(rs.getString(1));
            stmt= con.prepareStatement("SELECT type FROM asdproject.localbodytype where sl_no=?;");
            stmt.setInt(1,bt_id);
            rs = stmt.executeQuery();
            if(rs.next())
                tf_bodytype.setText(rs.getString(1));
            stmt= con.prepareStatement("SELECT p_name FROM asdproject.panchayaths where d_id=? and p_id=?;");
            stmt.setInt(1,d_id);
            stmt.setInt(2,b_id);
            rs = stmt.executeQuery();
            if(rs.next())
                tf_body.setText(rs.getString(1));
            }
        }
        catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } 
    }

