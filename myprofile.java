    private void b_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_updateActionPerformed
        // TODO add your handling code here:
        String u_email=tf_email.getText();
 //       String u_pass=tf_password.getText();
        String u_phno=tf_phonenumber.getText();
        String u_address=ta_address.getText();
        try
        {
            con = new Connector().getCon();
            stmt= con.prepareStatement("UPDATE `asdproject`.`citizen_list` SET `c_email` = ?, `c_pass` =?, `c_phno` = ?, `c_address` =? WHERE (`c_id` =?);");
            stmt.setInt(5,user_id);
            stmt.setString(1, u_email);
 //           stmt.setString(2, u_pass);
            stmt.setString(3, u_phno);
            stmt.setString(4, u_address);
            int rs = stmt.executeUpdate();
            if(rs!=0)
            {
                JOptionPane.showMessageDialog(null,"Profile sucessfully updated");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
            }
        }
        catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } finally {
            try {
                  if(con!=null) con.close();
                  
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
            }
            try {
                  if(stmt!=null) stmt.close();
                  
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
            }
            
        }
    }//GEN-LAST:event_b_updateActionPerformed

