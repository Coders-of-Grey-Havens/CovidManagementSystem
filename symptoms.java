    private void b_updatestatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_updatestatusActionPerformed
        // TODO add your handling code here:
        if(rb_fever_yes.isSelected()==true || rb_fatigue_yes.isSelected()==true || rb_cough_yes.isSelected()==true)
        {
           JOptionPane.showMessageDialog(null,"Please test yourself at nearest testing center at the earliest","Severe Symptoms Detected",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(rb_breathingissue_yes.isSelected()==true || rb_sorethroat_yes.isSelected()==true)
        {
            JOptionPane.showMessageDialog(null,"Quarantine Yourself or Do a Test","Mild Symptoms Detected",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(rb_headache_yes.isSelected()==true || rb_alteredsmell_yes.isSelected()==true || rb_alteredtaste_yes.isSelected()==true || rb_vomiting_yes.isSelected()==true || rb_nausea_yes.isSelected()==true)
        {
            JOptionPane.showMessageDialog(null,"Keep Social Distancing. In case of severity, do a test","Minor Symptoms Detected",JOptionPane.INFORMATION_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null,"Your health status seems to be normal.","No Symptoms Detected",JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_b_updatestatusActionPerformed

