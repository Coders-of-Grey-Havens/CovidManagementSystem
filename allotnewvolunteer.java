    private void b_assignasvolunteerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_assignasvolunteerActionPerformed
        try{
        int row = t_avolrequesttable.getSelectedRow();
        TableModel model = t_avolrequesttable.getModel();
        
        //System.out.println(row);
        
        Connection con = new Connector().getCon();
        if(cb_status.getSelectedItem().toString().equals("Accepted")){
            PreparedStatement ps2 = con.prepareStatement("SELECT * FROM `asdproject`.`volunteer_requests` vr WHERE vr.req_id=?");
            ps2.setString(1, model.getValueAt(row, 0).toString());
            ResultSet rs2 = ps2.executeQuery();
            rs2.first();
            PreparedStatement ps = con.prepareStatement("INSERT INTO `asdproject`.`volunteer_list` (`citizen_id`, `languages_known`, `alternate_phone`, `work_type`, `aadhar_number`) VALUES (?, ?, ?, ?, ?);");
            ps.setString(1, rs2.getString(2));
            ps.setString(2, rs2.getString(5));
            ps.setString(3, rs2.getString(6));
            ps.setString(4, rs2.getString(7));
            ps.setString(5, rs2.getString(9));
            int rs = ps.executeUpdate();
            if(rs!=0)
            {
               JOptionPane.showMessageDialog(null,"Alloted new Volunteer.");    
            }
            
        }
        PreparedStatement ps=con.prepareStatement("UPDATE `asdproject`.`volunteer_requests` SET `req_status` = ? WHERE (`req_id` = ?);");
        ps.setString(2, model.getValueAt(row, 0).toString());
        ps.setString(1, cb_status.getSelectedItem().toString());
        int rs = ps.executeUpdate();
        if(rs!=0)
        {
            tf_reqid.setText("");
            tf_avolname1.setText("");
            tf_wardnum.setText("");
            fillTable();
            JOptionPane.showMessageDialog(null,"Volunteer Status Updated");  
        }
        }catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } 
    }//GEN-LAST:event_b_assignasvolunteerActionPerformed

