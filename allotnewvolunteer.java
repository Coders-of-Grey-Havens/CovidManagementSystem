    private void fillTable(){
        try{
            Connection con = (Connection) new Connector().getCon();
            PreparedStatement ps = con.prepareStatement("SELECT vr.req_id,cl.c_fullname,vr.languages_known,vr.work_type,vr.aadhar_number,vr.preferred_ward,d.dist_name,p.p_name FROM `asdproject`.`volunteer_requests` vr, `asdproject`.`citizen_list` cl, `asdproject`.`districts` d, `asdproject`.`panchayaths` p WHERE vr.cit_id=cl.c_id AND cl.c_district_id=d.dist_id AND cl.c_local_body_name_id = p.p_id AND vr.req_status not like 'Accepted'");
            ResultSet rs = ps.executeQuery();
            t_avolrequesttable.setModel(DbUtils.resultSetToTableModel(rs));
             //System.out.println(rs.next());
            if(!rs.first()){
                JOptionPane.showMessageDialog(null," No new requests! All request status updated.");
            }
        }catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } 
        
    }
    
