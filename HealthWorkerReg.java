                                PreparedStatement stmt=con.prepareStatement("INSERT INTO `asdproject`.`health_worker_requests` (`cit_id`, `pan_number`, `alt_number`, `highest_qualification`, `institution_name`, `designation`, `aadhar_number`, `date_time`) VALUES (?, ?, ?, ?, ?, ?, ?, ?);");
                                stmt.setString(1,Integer.toString(id));
                                stmt.setString(2,tf_pan_number.getText());
                                stmt.setString(3,tf_alternate.getText());
                                stmt.setString(4,cb_highest_qualification.getSelectedItem().toString());
                                stmt.setString(5,tf_institution_name.getText());
                                stmt.setString(6,tf_designation.getText());
                                stmt.setString(7,tf_aadhaar.getText());
                                stmt.setString(8,today);
                                int flag = stmt.executeUpdate();
                                if(flag==1)
                                {
                                    JOptionPane.showMessageDialog(null,"Health Worker Application Recieved");
                                    this.dispose();
                                }
