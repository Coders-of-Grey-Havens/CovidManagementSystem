    private void fetch()
{
    try
    {
        Connection con=new Connector().getCon();
        PreparedStatement ps=con.prepareStatement("SELECT c_fullname, c_email, c_phno, c_district_id FROM asdproject.citizen_list WHERE c_id=?");
        ps.setString(1,Integer.toString(id));
        ResultSet rs=ps.executeQuery();
        if(rs.next())
        {
        tf_name.setText(rs.getString(1));
        tf_email.setText(rs.getString(2));
        tf_phno.setText(rs.getString(3));
        String dist=rs.getString(4);
        ps=con.prepareStatement("SELECT dist_name FROM asdproject.districts WHERE dist_id=?");
        ps.setString(1,dist);
        }
        rs=ps.executeQuery();
        if(rs.next())
        tf_district.setText(rs.getString(1));
        
    }
    catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } 
}

